void tick_screen_test() {
    void *flowState = getFlowState(0, 1);
    (void)flowState;
    {
        const char *new_val = evalTextProperty(flowState, 4, 3, "Failed to evaluate Text in Label widget");
        const char *cur_val = lv_label_get_text(objects.obj4);
        if (strcmp(new_val, cur_val) != 0) {
            tick_value_change_obj = objects.obj4;
            lv_label_set_text(objects.obj4, new_val);
            tick_value_change_obj = NULL;
        }
    }
    {
        int32_t new_val = evalIntegerProperty(flowState, 8, 3, "Failed to evaluate Value in Slider widget");
        int32_t cur_val = lv_slider_get_value(objects.test_slider);
        if (new_val != cur_val) {
            tick_value_change_obj = objects.test_slider;
            lv_slider_set_value(objects.test_slider, new_val, LV_ANIM_ON);
            tick_value_change_obj = NULL;
        }
    }
    {
        if (!(lv_obj_get_state(objects.obj8) & LV_STATE_EDITED)) {
            int32_t new_val = evalIntegerProperty(flowState, 16, 3, "Failed to evaluate Selected in Dropdown widget");
            int32_t cur_val = lv_dropdown_get_selected(objects.obj8);
            if (new_val != cur_val) {
                tick_value_change_obj = objects.obj8;
                lv_dropdown_set_selected(objects.obj8, new_val);
                tick_value_change_obj = NULL;
            }
        }
    }
}

